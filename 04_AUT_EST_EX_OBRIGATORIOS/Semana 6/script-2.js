
function clicarSoma() {
   var n1 = document.getElementById("V1").value;
   const n2 = 1
   document.querySelector(".resultado1").innerHTML = parseInt(n1) + parseInt(n2);
}

function clicarSub() {
    var n1 = document.getElementById("V1").value;
    const n2 = 1
    document.querySelector(".resultado2").innerHTML = parseInt(n1) - parseInt(n2);
}

